// Array destructuring
const numbers = [10, 20, 30];
const [first, second, third] = numbers;

console.log(first);  // 10
console.log(second); // 20

// Object destructuring
const user = {
  name: "Prithiv",
  age: 22,
  role: "Developer"
};

const { name, age, role } = user;

console.log(name); // Prithiv
console.log(age);  // 22